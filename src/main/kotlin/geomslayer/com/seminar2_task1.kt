import java.util.*

fun main(args: Array<String>) {
    val list = listOf(HelloPavel(), HelloRandom(), HelloRandom(), HelloRandom(), HelloKitty())
    for (hello in list) {
        hello.printMessage()
    }
}

abstract class HelloWorld {
    abstract fun printMessage()
}

class HelloPavel : HelloWorld() {
    override fun printMessage() {
        println("Hello, Pavel!")
    }
}

class HelloRandom : HelloWorld() {
    companion object {
        val names = arrayOf("Anya", "Katya", "Nastya", "Vasya", "Kolya", "Sonya")
        val rand = Random()
    }

    override fun printMessage() {
        println("Hello, ${getName()}!")
    }

    private fun getName(): String {
        return names[rand.nextInt(names.size)]
    }
}

